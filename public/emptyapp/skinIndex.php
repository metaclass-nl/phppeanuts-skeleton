<H1>Empty Application</H1>

If this page comes from your own web server you have successfully 
installed phpPeanuts. Congratulations!<BR>
<BR>
This application can be used to create a new application from scratch.<BR>
Just copy the 'emptyapp' folder from the phpPeanuts folder to become a 
new subfolder also directly in the phpPeanuts folder.<BR>
Then change all references to emptyapp in the files in the new folder 
to the name of the new folder.<BR>
Then create a new folder with the same name directly in the classes folder.<BR>
Finally make a new hyperlink and a new printSubmenu line in the 
'skinMenuPart.php' file in the includes folder referring the new folder.<BR>
<BR>
If you get this message properly from requesting the url referencing 
index.php in your new folder you have successfully created a new application.

